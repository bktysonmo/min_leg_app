-- db/extensions.sql
-- Persistent derived intelligence tables
-- NOT rebuilt on every analytics refresh — updated by intelligence pipelines
--
-- Canonical definitions for:
--   · ideology_scores        (PCA + clustering output)
--   · sponsorship_scores     (network centrality metrics)
--   · bill_lineage           (resurrection / similarity graph)
--   · caucus_clusters        (human-readable cluster labels)
--

PRAGMA foreign_keys = ON;

-- ─────────────────────────────────────────────────────────────────────────────
-- IDEOLOGY SCORES  (PCA + clustering output)
-- ─────────────────────────────────────────────────────────────────────────────
-- Replaces the split between member_ideology_profiles (analytics.sql) and
-- ideology_scores (extensions.sql). One table; analytics.sql no longer
-- defines member_ideology_profiles.

CREATE TABLE IF NOT EXISTS ideology_scores (
    member_id       INTEGER PRIMARY KEY REFERENCES members(member_id),
    session_id      TEXT,

    -- PCA axes (labels generated by the pipeline, not fixed)
    axis1_value     REAL,
    axis1_label     TEXT,   -- e.g. "Establishment ↔ Maverick"
    axis2_value     REAL,
    axis2_label     TEXT,   -- e.g. "Partisan ↔ Coalition Builder"
    axis3_value     REAL,   -- third axis when variance warrants it

    caucus_cluster  INTEGER REFERENCES caucus_clusters(cluster_id),
    ideology_label  TEXT,   -- synthesized human-readable label

    -- Derived scores
    polarity_score  REAL,   -- partisan polarity (-1 left … +1 right)
    maverick_score  REAL,   -- frequency of voting against party majority
    confidence      REAL,   -- model confidence 0–1

    updated_at      TEXT
);
CREATE INDEX IF NOT EXISTS idx_ideology_cluster ON ideology_scores(caucus_cluster);
CREATE INDEX IF NOT EXISTS idx_ideology_session ON ideology_scores(session_id);


-- ─────────────────────────────────────────────────────────────────────────────
-- SPONSORSHIP / NETWORK SCORES
-- ─────────────────────────────────────────────────────────────────────────────
-- Replaces the split between member_network_metrics (analytics.sql) and
-- sponsorship_scores (extensions.sql).

CREATE TABLE IF NOT EXISTS sponsorship_scores (
    member_id           INTEGER PRIMARY KEY REFERENCES members(member_id),
    session_id          TEXT,

    -- Graph centrality (computed over cosponsor_network)
    degree_centrality   REAL,
    betweenness         REAL,
    closeness           REAL,
    eigenvector         REAL,
    bridge_score        REAL,   -- structural hole / broker metric
    influence_score     REAL,   -- weighted composite

    -- Cross-party collaboration
    bipartisan_score    REAL,   -- cross-party cosponsor rate

    community_id        INTEGER,

    updated_at          TEXT
);
CREATE INDEX IF NOT EXISTS idx_sponsorship_influence ON sponsorship_scores(influence_score DESC);
CREATE INDEX IF NOT EXISTS idx_sponsorship_community ON sponsorship_scores(community_id);


-- ─────────────────────────────────────────────────────────────────────────────
-- BILL LINEAGE  (resurrection / similarity graph)
-- ─────────────────────────────────────────────────────────────────────────────
-- Canonical single definition. Columns unified from the two previously
-- conflicting definitions (source_bill_pk vs parent_bill_pk, etc.).

CREATE TABLE IF NOT EXISTS bill_lineage (
    lineage_id          INTEGER PRIMARY KEY AUTOINCREMENT,
    source_bill_pk      INTEGER REFERENCES bills(bill_pk),
    related_bill_pk     INTEGER REFERENCES bills(bill_pk),
    source_version_id   INTEGER REFERENCES bill_versions(version_id),
    related_version_id  INTEGER REFERENCES bill_versions(version_id),
    similarity_score    REAL,
    method              TEXT CHECK(method IN ('minhash','jaccard','hybrid','manual')),
    -- reintroduced / modeled_after / amended_from / substitute / copy / zombie
    relation_type       TEXT,
    detected_at         TEXT,
    UNIQUE(source_bill_pk, related_bill_pk)
);
CREATE INDEX IF NOT EXISTS idx_lineage_source ON bill_lineage(source_bill_pk);
CREATE INDEX IF NOT EXISTS idx_lineage_target ON bill_lineage(related_bill_pk);
CREATE INDEX IF NOT EXISTS idx_lineage_score  ON bill_lineage(similarity_score DESC);


-- ─────────────────────────────────────────────────────────────────────────────
-- CAUCUS CLUSTERS
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS caucus_clusters (
    cluster_id      INTEGER PRIMARY KEY,
    cluster_name    TEXT,
    description     TEXT,
    dominant_party  TEXT,
    size            INTEGER,
    session_id      TEXT,
    updated_at      TEXT
);


-- ─────────────────────────────────────────────────────────────────────────────
-- PERFORMANCE INDEXES
