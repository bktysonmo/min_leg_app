-- ─────────────────────────────────────────────────────────────────────────────
-- LANGUAGE FRAGMENTS
-- Sub-bill units of text (sections/passages) for partial-match lineage
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS bill_language_fragments (
    fragment_id     INTEGER PRIMARY KEY AUTOINCREMENT,
    bill_pk         INTEGER NOT NULL REFERENCES bills(bill_pk) ON DELETE CASCADE,
    version_id      INTEGER REFERENCES bill_versions(version_id),
    session_id      TEXT NOT NULL REFERENCES sessions(session_id),
    fragment_index  INTEGER NOT NULL,       -- ordinal within bill (0-based)
    fragment_type   TEXT,                   -- 'section' | 'paragraph' | 'passage'
    char_offset     INTEGER,                -- byte offset in full_text for reconstruction
    char_length     INTEGER,
    fragment_text   TEXT NOT NULL,
    content_hash    TEXT NOT NULL,          -- SHA-1 of normalized fragment text
    UNIQUE(version_id, fragment_index)
);
CREATE INDEX IF NOT EXISTS idx_frag_bill    ON bill_language_fragments(bill_pk);
CREATE INDEX IF NOT EXISTS idx_frag_session ON bill_language_fragments(session_id);
CREATE INDEX IF NOT EXISTS idx_frag_hash    ON bill_language_fragments(content_hash);

-- ─────────────────────────────────────────────────────────────────────────────
-- LANGUAGE LINEAGE  (extends bill_lineage with fragment-level and authorship)
-- ─────────────────────────────────────────────────────────────────────────────

CREATE TABLE IF NOT EXISTS language_lineage (
    lang_lineage_id         INTEGER PRIMARY KEY AUTOINCREMENT,

    -- Source: where the language originated
    source_bill_pk          INTEGER NOT NULL REFERENCES bills(bill_pk),
    source_version_id       INTEGER REFERENCES bill_versions(version_id),
    source_fragment_id      INTEGER REFERENCES bill_language_fragments(fragment_id),
    source_session_id       TEXT NOT NULL,
    source_member_id        INTEGER REFERENCES members(member_id),   -- primary sponsor

    -- Target: where the language reappears
    target_bill_pk          INTEGER NOT NULL REFERENCES bills(bill_pk),
    target_version_id       INTEGER REFERENCES bill_versions(version_id),
    target_fragment_id      INTEGER REFERENCES bill_language_fragments(fragment_id),
    target_session_id       TEXT NOT NULL,
    target_member_id        INTEGER REFERENCES members(member_id),   -- primary sponsor

    -- Match characteristics
    match_type              TEXT NOT NULL
        CHECK(match_type IN (
            'reintroduced',       -- whole bill, new session
            'amendment_adopted',  -- language added to another bill
            'partial_reuse',      -- section/passage reused
            'substitute',         -- bill replaced by similar language
            'zombie'              -- failed bill reappears later
        )),
    granularity             TEXT NOT NULL CHECK(granularity IN ('bill','section','passage')),
    similarity_score        REAL NOT NULL,      -- Jaccard (whole-bill) or containment (partial)
    containment_score       REAL,               -- |A∩B|/|A| — how much of source is in target
    method                  TEXT NOT NULL
        CHECK(method IN ('minhash','jaccard','containment','exact_hash','hybrid','manual')),

    -- Attribution chain (denormalized for query speed)
    all_source_member_ids   TEXT,   -- JSON array: primary + cosponsors of source bill
    all_target_member_ids   TEXT,   -- JSON array: primary + cosponsors of target bill

    detected_at             TEXT NOT NULL,
    confirmed               INTEGER,    -- NULL=auto / 1=human confirmed / 0=rejected
    confirmed_by            TEXT,
    note                    TEXT,

    UNIQUE(source_bill_pk, target_bill_pk, source_fragment_id, target_fragment_id)
);
CREATE INDEX IF NOT EXISTS idx_ll_source_bill    ON language_lineage(source_bill_pk);
CREATE INDEX IF NOT EXISTS idx_ll_target_bill    ON language_lineage(target_bill_pk);
CREATE INDEX IF NOT EXISTS idx_ll_source_session ON language_lineage(source_session_id);
CREATE INDEX IF NOT EXISTS idx_ll_target_session ON language_lineage(target_session_id);
CREATE INDEX IF NOT EXISTS idx_ll_source_member  ON language_lineage(source_member_id);
CREATE INDEX IF NOT EXISTS idx_ll_target_member  ON language_lineage(target_member_id);
CREATE INDEX IF NOT EXISTS idx_ll_match_type     ON language_lineage(match_type);
CREATE INDEX IF NOT EXISTS idx_ll_containment    ON language_lineage(containment_score DESC);